public class semanadiafor {
    public static void main(String[] args) {
        int dia = 0;
        for(dia=1; dia<=7; dia=dia+1){
            if(dia==1){
                System.out.println("lunes");
            }
            else if(dia==2){
                System.out.println("martes");
            }
            else if(dia==3){
                System.out.println("miercoles");
            }
        }
    }
    
}
