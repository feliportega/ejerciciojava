public class semanadiawhile {
    public static void main(String[] args) {
        int dia = 1;
        while(dia<=7){
            if(dia==1){

                System.out.println("lunes");
            }
            else if(dia==2){
                System.out.println("martes");
            }
            else if(dia==3){
                System.out.println("miercoles");
            }
            dia=dia+1;
            }
        }
    }
    

